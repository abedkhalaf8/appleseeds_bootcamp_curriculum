function describeCountry(country, population, capitalCity) {
    return `${country} has ${population} people and its capital city is ${capitalCity}`
}
console.log(describeCountry("israel", "10 milion", "tel aviv"));
console.log(describeCountry("canada", "40 milion", "toronto"));
console.log(describeCountry("jordan", "20 milion", "Amman"));

